salutations = ['hello', 'hi', 'howdy', 'heyas']

# The in operator checks if the chosen value is present in a given list
print('hello' in salutations)  # True
print('good morning' in  salutations)  # False

print()

# The not operator checks if the chosen value is NOT present in a given list, when accompanying the in operator
print('hello' not in salutations)  # False
print('good morning' not in  salutations)  # True
